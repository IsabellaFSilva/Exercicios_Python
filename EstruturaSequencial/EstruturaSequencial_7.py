"""
Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro
desta área para o usuário.

"""

l = int(input("Insira o tamanho do lado do quadrado: \n"))
a = l * l
dobroA = a * 2

print("O dobro da área do quadrado é %i cm \nÁrea do trabalho é %i cm" %(dobroA, a))